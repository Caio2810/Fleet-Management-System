    private void carregarDadosNaTabela() {
        try {
            controladora.carregarTabela(tabela1);
        } catch (Exception erro) {
            String msg = "Persistencia - Metodo Cadastrar Movimentacao - " + erro.getMessage();
            System.out.println(msg);
        }
    }

    private void controlarEstadoBotao(JButton bt) {
        boolean hasSelection = tabela1.getSelectedRow() != -1;

        bt.setEnabled(hasSelection);
    }

    private void telaVeiculosMouseClicked(java.awt.event.MouseEvent evt) {
        java.awt.Component clickedComponent = evt.getComponent().getComponentAt(evt.getPoint());

        if (clickedComponent != tabela1 && clickedComponent != jScrollPane1) {
            tabela1.clearSelection();
        }
    }

