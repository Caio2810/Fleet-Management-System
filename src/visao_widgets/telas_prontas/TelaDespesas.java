    private void btDeletarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btDeletarActionPerformed
        int selectedRow = tabela1.getSelectedRow();

        if (selectedRow < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione uma despesa para deletar!",
                    "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        Object id = tabela1.getValueAt(selectedRow, 0);
        String despesaID = id.toString();

        try {
            controladora.removerDespesa(despesaID, tabela1);

            javax.swing.JOptionPane.showMessageDialog(this, "Sua despesa foi deletada com sucesso.",
                    "Sucesso!", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception erro) {
            String msg = "Persistência - Método Deletar Despesa - " + erro.getMessage();
            System.out.println(msg);

            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao deletar despesa:\n" + erro.getMessage(),
                    "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }// GEN-LAST:event_btDeletarActionPerformed

    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btEditarActionPerformed
        int selectedRow = tabela1.getSelectedRow();

        if (selectedRow < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um veículo para editar!",
                    "Atenção", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        Object id = tabela1.getValueAt(selectedRow, 0);
        String despesaID = id.toString();

        try {
            visao_widgets.paineis_de_telas.EditarDespesaPanel formPanel = new visao_widgets.paineis_de_telas.EditarDespesaPanel(
                    tabela1);

            formPanel.carregarDados(despesaID);

            javax.swing.JDialog dialog = new javax.swing.JDialog(
                    (java.awt.Frame) javax.swing.SwingUtilities.getWindowAncestor(this),
                    "Editar Despesa: " + despesaID,
                    true // Modal
            );

            dialog.setContentPane(formPanel);
            dialog.pack();
            dialog.setLocationRelativeTo(this);
            dialog.setVisible(true);

            carregarDadosNaTabela();

        } catch (Exception erro) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Erro ao carregar ou editar dados da despesa:\n" + erro.getMessage(),
                    "Erro",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }// GEN-LAST:event_btEditarActionPerformed

