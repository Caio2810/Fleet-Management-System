        private void configurarFiltroPlaca() {
                ((AbstractDocument) textFieldPlaca.getDocument()).setDocumentFilter(new DocumentFilter() {
                        @Override
                        public void insertString(FilterBypass fb, int offset, String string, AttributeSet attr)
                                        throws BadLocationException {
                                if (string == null)
                                        return;
                                String text = string.replaceAll("[^a-zA-Z0-9]", "").toUpperCase();
                                if ((fb.getDocument().getLength() + text.length()) <= 7) {
                                        super.insertString(fb, offset, text, attr);
                                }
                        }

                        @Override
                        public void replace(FilterBypass fb, int offset, int length, String text, AttributeSet attrs)
                                        throws BadLocationException {
                                if (text == null)
                                        return;
                                String cleanedText = text.replaceAll("[^a-zA-Z0-9]", "").toUpperCase();
                                if ((fb.getDocument().getLength() + cleanedText.length() - length) <= 7) {
                                        super.replace(fb, offset, length, cleanedText, attrs);
                                }
                        }
                });
        }

