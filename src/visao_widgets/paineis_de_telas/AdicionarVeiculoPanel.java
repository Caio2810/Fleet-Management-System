        private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
                String placa = textFieldPlaca.getText().trim().toUpperCase();
                String marca = comboBoxMarca.getSelectedItem().toString();
                String modelo = capitalizeWords(textFieldModelo.getText());
                String anoStr = textFieldAno.getText().trim();

                EstadoDoVeiculo estado = EstadoDoVeiculo.valueOf(jComboBox1.getSelectedItem().toString());

                // Validação de campos vazios ou não selecionados
                if (placa.isEmpty() || marca.equals("Selecione...") || modelo.isEmpty() || anoStr.isEmpty()) {
                        JOptionPane.showMessageDialog(this,
                                        "Todos os campos (Placa, Marca, Modelo, Ano) devem ser preenchidos/selecionados.",
                                        "Erro de Validação", JOptionPane.WARNING_MESSAGE);
                        return;
                }

                // EXPRESSÃO REGULAR DA PLACA:
                // ^[A-Z]{3} -> Começa obrigatoriamente com 3 letras
                // [0-9] -> Seguido por 1 número
                // [A-Z0-9] -> 5ª posição aceita letra (Mercosul) ou número (Tradicional)
                // [0-9]{2}$ -> Termina estritamente com 2 números
                if (!placa.matches("^[A-Z]{3}[0-9][A-Z0-9][0-9]{2}$")) {
                        JOptionPane.showMessageDialog(this,
                                        "Formato de Placa Inválido!\nExemplos aceitos:\n- Tradicional: ABC1234\n- Mercosul: ABC1D23",
                                        "Erro de Validação", JOptionPane.WARNING_MESSAGE);
                        return;
                }

                if (anoStr.length() != 4) {
                        JOptionPane.showMessageDialog(this,
                                        "O campo Ano deve conter exatamente 4 dígitos (ex: 2026).",
                                        "Erro de Validação", JOptionPane.WARNING_MESSAGE);
                        return;
                }

                try {
                        int ano = Integer.parseInt(anoStr);
                        controladora.incluirVeiculo(placa, marca, modelo, ano, estado, tabelaVeiculos);

                        JDialog dialog = (JDialog) SwingUtilities.getWindowAncestor(this);
                        dialog.dispose();

                        JOptionPane.showMessageDialog(null,
                                        "Dados do veículo atualizados com sucesso!",
                                        "Sucesso", JOptionPane.INFORMATION_MESSAGE);

                } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(this,
                                        "Erro: O campo Ano deve ser um número inteiro (ex: 2026).",
                                        "Erro de Formato", JOptionPane.ERROR_MESSAGE);
                } catch (Exception e) {
                        JOptionPane.showMessageDialog(this,
                                        "Erro ao atualizar veículo:\n" + e.getMessage(),
                                        "Erro de Persistência", JOptionPane.ERROR_MESSAGE);
                }
        }

        public static String capitalizeWords(String str) {
                if (str == null || str.isEmpty())
                        return str;

                String[] words = str.trim().toLowerCase().split("\\s+");
                StringBuilder capitalized = new StringBuilder();

                for (String word : words) {
                        if (word.length() > 0) {
                                capitalized.append(Character.toUpperCase(word.charAt(0)))
                                                .append(word.substring(1))
                                                .append(" ");
                        }
                }

                return capitalized.toString().trim();
        }

